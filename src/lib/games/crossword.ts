export function computeCrosswordScore(
    durationSeconds: number,
    checksUsed: number,
    revealsUsed: number
): number {
    const timePenalty = Math.floor(durationSeconds / 30);
    return Math.max(20, 100 - timePenalty - checksUsed * 4 - revealsUsed * 10);
}
