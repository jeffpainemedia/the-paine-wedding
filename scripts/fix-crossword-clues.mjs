const re = /\{ word: "([A-Z]+)", clue: "(\d+ letters?)"/g;
const missing = new Set();
let replaced = 0;

const out = src.replace(re, (match, word) => {
    const clue = CLUES[word];
    if (!clue) {
        missing.add(word);
        return match;
    }
    replaced++;
    return `{ word: "${word}", clue: "${clue}"`;
});

